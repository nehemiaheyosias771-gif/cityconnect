/**
 * Simple translation hook — returns t(key) function for current language.
 * Usage: const t = useT(); t('map.title')
 */
export function getT(lang = 'en') {
  const dict = translations[lang] || translations.en;
  return (path) => {
    const keys = path.split('.');
    let val = dict;
    for (const k of keys) {
      val = val?.[k];
      if (val === undefined) {
        // Fallback to English
        let fb = translations.en;
        for (const fk of keys) fb = fb?.[fk];
        return fb || path;
      }
    }
    return val || path;
  };
}

